    private static void changeSubElements(String langu, JPanel panel) {

        for (Component jc : panel.getComponents()) {
            if (jc instanceof JLabel) {

                JLabel lbl = (JLabel) jc;

                if (lbl.getText().equals("Insert") || lbl.getText().equals("")) {
                    if (langu.equals("SIN")) {

                        lbl.setText("");
                    } else {

                        lbl.setText("Insert");
                    }

                } else if (lbl.getText().equals("Update") || lbl.getText().equals("")) {
                    if (langu.equals("SIN")) {

                        lbl.setText("");
                    } else {

                        lbl.setText("Update");
                    }

                } else if (lbl.getText().equals("Search") || lbl.getText().equals("")) {
                    if (langu.equals("SIN")) {

                        lbl.setText("");
                    } else {
                        lbl.setText("Search");

                    }

                }
            }
        }

    }

