    private void com_btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_com_btnSaveMouseClicked

        try {
            ArrayList<String> err = checkFields();

            //not updateble
            if (err.isEmpty()) {

                if (!isUpdateable) {

                    company = new MyCompanyDetails();
                }
                //save

                company.setImg1(imgpath2);
                company.setImg2(imgpath);
                company.setName(com_basic_name.getText());
                company.setRegNo(com_basic_br.getText());

                company.setNo(com_ad_no.getText());
                company.setStreet(com_ad_street.getText());
                company.setCity(com_ad_city.getText());
                company.setCountry(com_ad_country.getText());

                boolean isInt = false;

                try {

                    if (!com_co_mob1.getText().isEmpty()) {

                        company.setMobile1(Integer.parseInt(com_co_mob1.getText()));
                    }
                    if (!com_co_mob2.getText().isEmpty()) {

                        company.setMobile2(Integer.parseInt(com_co_mob2.getText()));
                    }
                    if (!com_con_land1.getText().isEmpty()) {
                        company.setTp1(Integer.parseInt(com_con_land1.getText()));
                    }
                    if (!com_con_land2.getText().isEmpty()) {
                        company.setTp2(Integer.parseInt(com_con_land2.getText()));
                    }

                    isInt = true;
                } catch (NumberFormatException e) {
                    isInt = false;
                }
                company.setEmail(com_con_email.getText());
                company.setHeading1(com_con_emailPass.getText());
                company.setWebsite(com_con_web.getText());

                if (isInt) {
                    deactiveOthers();

                    company.setStatus("active");

                    if (!isUpdateable) {

                        EXTCLASS.getHibSession().save(company);
                        EXTCLASS.showMessage("Record added!", "දත්ත එක් කළා !", "SUCCESS", Application.SINSUCCESS, 1);

                    } else {
                        EXTCLASS.getHibSession().update(company);
                        EXTCLASS.showMessage("Record updated!", "දත්ත යාවත්කාලීන කරන ලදි !", "SUCCESS", Application.SINSUCCESS, 1);
                    }

                    Transaction t = EXTCLASS.getHibSession().beginTransaction();
                    t.commit();

                    loadTbl(c);
                    clearFields();
                } else {
                    EXTCLASS.showMessage("Enter numbers only!", "අංක පමණක් ඇතුළත් කරන්න", "ERROR", Application.SINERROR, 0);

                }

            } else {
                String erros = "";
                int i = 0;
                for (String e : err) {
                    erros += e;
                    i++;
                    erros += (err.size() != i) ? "," : ".";

                }
                EXTCLASS.showMessage("Fill following fields " + erros, "කරුණාකර පහත දැක්වෙන විස්තර පුරවන්න " + erros, "ERROR", Application.SINERROR, 0);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_com_btnSaveMouseClicked
