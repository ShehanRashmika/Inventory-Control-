    private void loadTbl(Criteria c) {

        try {

            DefaultTableModel dtm = (DefaultTableModel) comAllList.getModel();
            dtm.setRowCount(0);

            if (!c.list().isEmpty()) {

                List<MyCompanyDetails> list = c.list();

                for (MyCompanyDetails myCompanyDetails : list) {
                    Vector v = new Vector();
                    v.add(myCompanyDetails.getIdcompanyDetails());
                    v.add(myCompanyDetails.getName());
                    v.add(myCompanyDetails.getRegNo());
                    v.add(myCompanyDetails.getEmail());
                    v.add(myCompanyDetails.getWebsite());
                    v.add(myCompanyDetails.getStatus());
                    dtm.addRow(v);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void deactiveOthers() {

        try {

            //deactive all other records
            Criteria c = EXTCLASS.getHibSession().createCriteria(MyCompanyDetails.class);
            if (!c.list().isEmpty()) {

                List<MyCompanyDetails> list = c.list();

                for (MyCompanyDetails myCompanyDetails : list) {

                    myCompanyDetails.setStatus("deactive");

                    EXTCLASS.getHibSession().update(myCompanyDetails);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

