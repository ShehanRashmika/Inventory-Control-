    private void com_findbyNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_com_findbyNameKeyReleased

        try {
            if (!com_findbyName.getText().isEmpty()) {

                String q = "FROM SupAndVendorCompanyDetails WHERE idsupAndVendorCompanyDetails like concat('%',:idsupAndVendorCompanyDetails,'%') OR name like concat('%',:name,'%') ";
                Query query;

                if (com_findActive.isSelected()) {

                    q += " and status =:st";
                    query = EXTCLASS.getHibSession().createQuery(q);
                    query.setParameter("st", "active");
                } else if (com_findInactive.isSelected()) {

                    q += " and status =:st";
                    query = EXTCLASS.getHibSession().createQuery(q);
                    query.setParameter("st", "deactive");
                } else {
                    query = EXTCLASS.getHibSession().createQuery(q);
                }

                query.setParameter("idsupAndVendorCompanyDetails", com_findbyName.getText());
                query.setParameter("name", com_findbyName.getText());

                DefaultTableModel dtm = (DefaultTableModel) com_findAllTbl.getModel();
                dtm.setRowCount(0);

                if (!query.list().isEmpty()) {

                    List<SupAndVendorCompanyDetails> list = query.list();

                    for (SupAndVendorCompanyDetails myCompanyDetails : list) {
                        Vector v = new Vector();
                        v.add(myCompanyDetails.getIdsupAndVendorCompanyDetails());
                        v.add(myCompanyDetails.getName());
                        v.add(myCompanyDetails.getCity());
                        v.add(myCompanyDetails.getMobile1());
                        v.add(myCompanyDetails.getTp1());
                        v.add(myCompanyDetails.getEmail());
                        v.add(myCompanyDetails.getStatus());
                        dtm.addRow(v);
                    }

                }

            } else {

                if (com_findActive.isSelected()) {

                    changeComState("active", "company");

                } else if (com_findInactive.isSelected()) {

                    changeComState("deactive", "company");
                } else {

                    loadTbl(c);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_com_findbyNameKeyReleased

