    private void changeComState(String st, String tb) {

        try {
            Criteria c = null;
            if (tb.equals("company")) {

                c = EXTCLASS.getHibSession().createCriteria(SupAndVendorCompanyDetails.class);
                c.add(Restrictions.eq("status", st));
                loadTbl(c);

            } else if (tb.equals("supplier")) {

                c = EXTCLASS.getHibSession().createCriteria(Supplier.class);
                c.add(Restrictions.eq("status", st));
                loadSupTbl(c);
            } else if (tb.equals("vendor")) {

                c = EXTCLASS.getHibSession().createCriteria(Vendor.class);
                c.add(Restrictions.eq("status", st));
                loadVenTbl(c);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadCompanyCombo() {

        try {

            Criteria c = EXTCLASS.getHibSession().createCriteria(SupAndVendorCompanyDetails.class);
            c.add(Restrictions.eq("status", "active"));

            if (!c.list().isEmpty()) {

                List<SupAndVendorCompanyDetails> list = c.list();
                Vector v = new Vector();
                for (SupAndVendorCompanyDetails sup : list) {

                    v.add(sup.getName() + "-" + sup.getIdsupAndVendorCompanyDetails());

                }
                sup_companyList.setModel(new DefaultComboBoxModel(v));
                ven_companyList.setModel(new DefaultComboBoxModel(v));
            }

        } catch (Exception e) {

            e.printStackTrace();
        }

    }

